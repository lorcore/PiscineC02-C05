#include <unistd.h>

void	func(int *nb)
{
	int		d;
	char	c;

	d = 1000000000;
	while (d > 0 && (*nb) != -2147483648)
	{
		if ((*nb) / d != 0 || d == 1)
		{
			c = '0' + (*nb / d % 10);
			write(1, &c, 1);
		}
		d /= 10;
	}
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (nb < 0 && nb != -2147483648)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	func(&nb);
}